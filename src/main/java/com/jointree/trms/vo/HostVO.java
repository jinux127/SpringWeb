package com.jointree.trms.vo;

public class HostVO {
	
	private String hostSeq;
	private String hostName;
	private String hostTel;
	private String hostStatus;
	private String token;
	private String failureCnt;
	
	
	public String getHostSeq() {
		return hostSeq;
	}
	public void setHostSeq(String hostSeq) {
		this.hostSeq = hostSeq;
	}
	public String getHostName() {
		return hostName;
	}
	public void setHostName(String hostName) {
		this.hostName = hostName;
	}
	public String getHostTel() {
		return hostTel;
	}
	public void setHostTel(String hostTel) {
		this.hostTel = hostTel;
	}
	public String getHostStatus() {
		return hostStatus;
	}
	public void setHostStatus(String hostStatus) {
		this.hostStatus = hostStatus;
	}
	public String getToken() {
		return token;
	}
	public void setToken(String token) {
		this.token = token;
	}
	public String getFailureCnt() {
		return failureCnt;
	}
	public void setFailureCnt(String failureCnt) {
		this.failureCnt = failureCnt;
	}

	
	
}
