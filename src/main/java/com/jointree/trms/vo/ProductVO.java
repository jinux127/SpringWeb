package com.jointree.trms.vo;

public class ProductVO {
	private String productSeq;
	private String productName;
	private String productStatus;
	private String price;
	private String maxNumberPerson;
	private String productInfo;
	private String businessStartTime;
	private String businessEndTime;
	private String unitTime;
	private String workplaceSeq;
	
	public String getProductSeq() {
		return productSeq;
	}
	public void setProductSeq(String productSeq) {
		this.productSeq = productSeq;
	}
	public String getProductName() {
		return productName;
	}
	public void setProductName(String productName) {
		this.productName = productName;
	}
	public String getProductStatus() {
		return productStatus;
	}
	public void setProductStatus(String productStatus) {
		this.productStatus = productStatus;
	}
	public String getPrice() {
		return price;
	}
	public void setPrice(String price) {
		this.price = price;
	}
	public String getMaxNumberPerson() {
		return maxNumberPerson;
	}
	public void setMaxNumberPerson(String maxNumberPerson) {
		this.maxNumberPerson = maxNumberPerson;
	}
	public String getProductInfo() {
		return productInfo;
	}
	public void setProductInfo(String productInfo) {
		this.productInfo = productInfo;
	}
	public String getBusinessStartTime() {
		return businessStartTime;
	}
	public void setBusinessStartTime(String businessStartTime) {
		this.businessStartTime = businessStartTime;
	}
	public String getBusinessEndTime() {
		return businessEndTime;
	}
	public void setBusinessEndTime(String businessEndTime) {
		this.businessEndTime = businessEndTime;
	}
	public String getUnitTime() {
		return unitTime;
	}
	public void setUnitTime(String unitTime) {
		this.unitTime = unitTime;
	}
	public String getWorkplaceSeq() {
		return workplaceSeq;
	}
	public void setWorkplaceSeq(String workplaceSeq) {
		this.workplaceSeq = workplaceSeq;
	}


}
